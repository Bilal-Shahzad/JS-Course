// The decrement operator (--) decrements (subtracts one from) its operand and returns a value.

// Postfix decrement
let x = 3;
y = x--;
console.log(x)

// Prefix decrement
let a = 2;
b = --a;


let g = 3;
const q = g--;
console.log(g);
console.log(q);



let c = 3;
const d = --c;
console.log(c);
console.log(d);



